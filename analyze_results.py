_EXCLUDED_CONFIG_KEYS = ["name", "verbose", "polarity", "created_at"]

@dataclass
class ExperimentResult:
    """Represents a single iteration of an experiment"""
    iteration: int
    accuracy: Optional[float]
    flops: Optional[float]
    nparams: Optional[float]
    latency: Optional[float]
    hw_cost: Optional[float]
    hw_total_cost: Optional[float]
    hw_config: Optional[str]
    score: Optional[float]
    # hyperparams: Optional[Dict[str, Any]]
    score: Optional[float] = None  # Calculated as -accuracy if no modules are present


class ResultsAnalyzer:
    """Analyzer for experiment results"""
    
    def __init__(self, experiment_dir: str):
        """
        Initialize the analyzer.
        
        Args:
            experiment_dir: Experiment folder containing algorithm_logs/
        """
        self.experiment_dir = Path(experiment_dir)
        self.algorithm_logs_dir = self.experiment_dir / "algorithm_logs"
        self.results: List[ExperimentResult] = []
        self.has_flops_module = False
        self.has_hardware_module = False
        self.config: Dict[str, Any] = {}  # Experiment configuration
        
    def load_results(self) -> bool:
        """
        Load all results from log files.
        
        Returns:
            True if logs were loaded successfully, False otherwise
        """
        # Load config.yaml file
        self._load_config_yaml()
        
        if not self.algorithm_logs_dir.exists():
            print(f"  algorithm_logs folder not found in {self.experiment_dir}")
            return False
        
        # Load accuracies
        accuracies = self._load_accuracies()
        if not accuracies:
            print(f"  No acc_report.txt file found")
            return False
        
        scores = self._load_scores()
        if scores:
            print(f"  score_report.txt file found, will use scores calculated during training")
        else:
            print(f"  No score_report.txt file found, will use -accuracy as approximation")
        
        # Load hyperparameters
        # hyperparams_list = self._load_hyperparams()
        
        # Load FLOPS data
        flops_data = self._load_flops_data()
        if flops_data:
            self.has_flops_module = True
        
        # Load hardware data
        hw_data = self._load_hardware_data()
        if hw_data:
            self.has_hardware_module = True
        
        # Combine the data
        max_iterations = max(
            len(accuracies),
            # len(hyperparams_list),
            len(flops_data) if flops_data else 0,
            len(hw_data) if hw_data else 0
        )
        
        for i in range(max_iterations):
            result = ExperimentResult(
                iteration=i + 1,
                accuracy=accuracies[i] if i < len(accuracies) else None,
                flops=flops_data[i][1] if flops_data and i < len(flops_data) else None,
                nparams=flops_data[i][0] if flops_data and i < len(flops_data) else None,
                latency=hw_data[i][0] if hw_data and i < len(hw_data) else None,
                hw_cost=hw_data[i][1] if hw_data and i < len(hw_data) else None,
                hw_total_cost=hw_data[i][2] if hw_data and i < len(hw_data) else None,
                hw_config=hw_data[i][3] if hw_data and i < len(hw_data) else None,
                score=scores[i] if scores and i < len(scores) else None,
                # hyperparams=hyperparams_list[i] if i < len(hyperparams_list) else None,
            )
            
            # Calculate score: -accuracy if no modules are present
            if result.score is None:
                if result.accuracy is not None:
                    result.score = -result.accuracy
                else:
                    result.score = None
            
            self.results.append(result)
        
        return True
    
    def _load_accuracies(self) -> List[Optional[float]]:
        """Load accuracies from acc_report.txt file"""
        acc_file = self.algorithm_logs_dir / "acc_report.txt"
        if not acc_file.exists():
            return []
        
        accuracies = []
        try:
            with open(acc_file, 'r') as f:
                for line in f:
                    line = line.strip()
                    if line == "None" or line == "":
                        accuracies.append(None)
                    else:
                        accuracies.append(float(line))
        except Exception as e:
            print(f"  Error reading {acc_file}: {e}")
            return []
        
        return accuracies
    
    def _load_scores(self) -> List[Optional[float]]:
        """Load scores from score_report.txt file"""
        score_file = self.algorithm_logs_dir / "score_report.txt"
        if not score_file.exists():
            return []
        
        scores = []
        try:
            with open(score_file, 'r') as f:
                for line in f:
                    line = line.strip()
                    if line == "None" or line == "":
                        scores.append(None)
                    else:
                        scores.append(float(line))
        except Exception as e:
            print(f"  Error reading {score_file}: {e}")
            return []
        
        return scores
    
    def _load_hyperparams(self) -> List[Optional[Dict[str, Any]]]:
        """Load hyperparameters from hyper-neural.txt file"""
        hp_file = self.algorithm_logs_dir / "hyper-neural.txt"
        if not hp_file.exists():
            return []
        
        hyperparams = []
        try:
            with open(hp_file, 'r') as f:
                for line in f:
                    line = line.strip()
                    if not line:
                        hyperparams.append(None)
                        continue
                    try:
                        # Parse Python dict
                        hp_dict = ast.literal_eval(line)
                        hyperparams.append(hp_dict)
                    except Exception as e:
                        print(f"  Error parsing hyperparameters: {e}")
                        hyperparams.append(None)
        except Exception as e:
            print(f"  Error reading {hp_file}: {e}")
            return []
        
        return hyperparams
    
    def _load_flops_data(self) -> Optional[List[Tuple[float, float]]]:
        """Load FLOPS data from flops_report.txt file"""
        flops_file = self.algorithm_logs_dir / "flops_report.txt"
        if not flops_file.exists():
            flops_file = self.algorithm_logs_dir / "params_report.txt"
            if not flops_file.exists():
                return None

        flops_data = []
        try:
            with open(flops_file, 'r') as f:
                for line in f:
                    line = line.strip()
                    if not line:
                        continue
                    parts = line.split(' ')
                    if len(parts) >= 2:
                        flops_data.append((float(parts[0]), float(parts[1])))
                    else:
                        # If it's a file with only params, add None for FLOPS
                        flops_data.append((float(parts[0]), None))
        except Exception as e:
            print(f"  Error reading {flops_file}: {e}")
            return None
        
        return flops_data if flops_data else None
    
    def _load_hardware_data(self) -> Optional[List[Tuple[float, float, float, str]]]:
        """Load hardware data from hardware_report.txt file"""
        hw_file = self.algorithm_logs_dir / "hardware_report.txt"
        if not hw_file.exists():
            return None
        
        hw_data = []
        try:
            with open(hw_file, 'r') as f:
                for line in f:
                    line = line.strip()
                    if not line:
                        continue
                    parts = line.split(',')
                    if len(parts) >= 4:
                        hw_data.append((
                            float(parts[0]),  # latency
                            float(parts[1]),  # cost
                            float(parts[2]),  # total_cost
                            parts[3].strip()   # config
                        ))
        except Exception as e:
            print(f"  Error reading {hw_file}: {e}")
            return None
        
        return hw_data if hw_data else None
    
    def _load_config_yaml(self) -> None:
        """Load configuration from config.yaml file"""
        config_file = self.experiment_dir / "config.yaml"
        if not config_file.exists():
            print(f"  config.yaml file not found in {self.experiment_dir}")
            self.config = {}
            return
        
        try:
            with open(config_file, 'r') as f:
                self.config = yaml.safe_load(f) or {}
        except Exception as e:
            print(f"  Error reading config.yaml: {e}")
            self.config = {}

    def get_best_result(self) -> Optional[ExperimentResult]:
        """Return the best result (lowest score)"""
        valid_results = [r for r in self.results if r.score is not None]
        if not valid_results:
            return None
        return min(valid_results, key=lambda r: r.score)
    
    def save_experiment_csv(self, output_csv: Path) -> bool:
        """
        Save the experiment results to CSV.
        
        Args:
            output_csv: Path of the output CSV file
            
        Returns:
            True if save was successful
        """
        if not self.results:
            print(f"  No results to save")
            return False
        
        try:
            with open(output_csv, 'w', newline='') as f:
                # Determine columns dynamically
                fieldnames = [
                    'iteration', 'accuracy', 'score',
                    'nparams', 'flops',
                    'latency', 'hw_cost', 'hw_total_cost', 'hw_config'
                ]
                
                # Add fields for hyperparameters
                all_hp_keys = set()
                # for result in self.results:
                #     if result.hyperparams:
                #         all_hp_keys.update(result.hyperparams.keys())
                
                fieldnames.extend(sorted(all_hp_keys))
                
                # Add fields from config.yaml
                if self.config:
                    config_keys = sorted([k for k in self.config.keys() if k not in fieldnames])
                    fieldnames.extend(config_keys)
                
                writer = csv.DictWriter(f, fieldnames=fieldnames)
                writer.writeheader()
                
                for result in self.results:
                    row = {
                        'iteration': result.iteration,
                        'accuracy': result.accuracy,
                        'score': result.score,
                        'nparams': result.nparams,
                        'flops': result.flops,
                        'latency': result.latency,
                        'hw_cost': result.hw_cost,
                        'hw_total_cost': result.hw_total_cost,
                        'hw_config': result.hw_config,
                    }
                    
                    # Add hyperparameters
                    # if result.hyperparams:
                    #     for key in all_hp_keys:
                    #         row[key] = result.hyperparams.get(key, '')
                    
                    # Add data from config.yaml
                    for key, value in self.config.items():
                        if key not in row:
                            row[key] = value
                    
                    writer.writerow(row)
            
            return True
        except Exception as e:
            print(f"  Error saving CSV: {e}")
            return False

def _calculate_flops(exp_dir: Path) -> Optional[float]:
    """Load the best model from exp_dir/Model/best-model.keras and compute FLOPs."""
    try:
        from tensorflow import keras
    except Exception as e:
        print(f"  TensorFlow not available, cannot calculate FLOPS: {e}")
        return None

    model_path = exp_dir / "Model" / "best-model.keras"
    if not model_path.exists():
        print(f"  Model file not found: {model_path}")
        return None

    try:
        model = keras.models.load_model(model_path, compile=False)
        flops, _ = analyze_model(model)
        return flops.total_float_ops
    except Exception as e:
        print(f"  Error calculating FLOPS from model: {e}")
        return None

def _extract_shape(shape_obj: Any) -> Optional[List[int]]:
    """Normalize keras/tensor shapes to a plain list of ints."""
    if shape_obj is None:
        return None

    shape = shape_obj
    if hasattr(shape_obj, "shape"):
        shape = shape_obj.shape

    if isinstance(shape, (list, tuple)) and shape and isinstance(shape[0], (list, tuple)):
        shape = shape[0]

    try:
        normalized = [int(dim) if dim is not None else None for dim in shape]
    except Exception:
        return None

    return normalized


def _build_model_specs(model: Any) -> Dict[str, LayerSpec]:
    """Build minimal LayerSpec map required by the NVDLA profiler."""
    try:
        from tensorflow.keras.layers import Conv2D, Dense
    except Exception:
        return {}

    specs: Dict[str, LayerSpec] = {}
    for layer in model.layers:
        if isinstance(layer, Conv2D):
            in_shape = _extract_shape(layer.input)
            out_shape = _extract_shape(layer.output)
            if not in_shape or not out_shape or len(in_shape) < 4 or len(out_shape) < 4:
                continue
            if any(v is None for v in (in_shape[1], in_shape[2], in_shape[3], out_shape[1], out_shape[2], out_shape[3])):
                continue
            specs[layer.name] = LayerSpec(
                name=layer.name,
                type=LayerTypes.Conv2D,
                module=layer,
                params={
                    Params.IN_CHANNELS: in_shape[3],
                    Params.IN_HEIGHT: in_shape[1],
                    Params.IN_WIDTH: in_shape[2],
                    Params.OUT_CHANNELS: out_shape[3],
                    Params.OUT_HEIGHT: out_shape[1],
                    Params.OUT_WIDTH: out_shape[2],
                    Params.KERNEL_SIZE: layer.kernel_size,
                    Params.STRIDE: layer.strides,
                    Params.PADDING: layer.padding,
                    Params.BIAS: layer.use_bias,
                }
            )
        elif isinstance(layer, Dense):
            in_shape = _extract_shape(layer.input)
            out_shape = _extract_shape(layer.output)
            if not in_shape or not out_shape or len(in_shape) < 2 or len(out_shape) < 2:
                continue
            if any(v is None for v in (in_shape[-1], out_shape[-1])):
                continue
            specs[layer.name] = LayerSpec(
                name=layer.name,
                type=LayerTypes.Dense,
                module=layer,
                params={
                    Params.IN_FEATURES: in_shape[-1],
                    Params.OUT_FEATURES: out_shape[-1],
                    Params.BIAS: layer.use_bias,
                }
            )

    return specs


def _calculate_hardware(exp_dir: Path) -> Optional[Tuple[float, float, float, str]]:
    """Estimate latency/cost/total_cost/config for exp_dir/Model/best-model.keras."""
    try:
        from tensorflow import keras
    except Exception as e:
        print(f"  TensorFlow not available, cannot calculate hardware metrics: {e}")
        return None

    model_path = exp_dir / "Model" / "best-model.keras"
    if not model_path.exists():
        print(f"  Model file not found for hardware estimate: {model_path}")
        return None

    try:
        model = keras.models.load_model(model_path, compile=False)
    except Exception as e:
        print(f"  Error loading model for hardware estimate: {e}")
        return None

    model_specs = _build_model_specs(model)
    if not model_specs:
        print("  Hardware estimate skipped: no supported Conv2D/Dense layers found")
        return None

    specs_dir = Path(__file__).resolve().parent / "nvdla" / "specs"
    nvdla_list = [
        {"name": "nv_small", "path": "nv_small64_fp32.yaml", "area": 2.824},
        {"name": "nv_small256", "path": "nv_small256_fp32.yaml", "area": 3.091},
        {"name": "nv_large", "path": "nv_large2048_fp32.yaml", "area": 3.809},
    ]

    cost_par = 10000.0
    weight_cost = 0.7
    max_latency = 0.008
    max_cost = 30000.0
    log_file = "profiler_logs.txt"

    best_hw: Optional[Tuple[float, float, float, str]] = None
    for cfg in nvdla_list:
        cfg_path = specs_dir / cfg["path"]
        if not cfg_path.exists():
            continue

        try:
            nvdla_profiler = profiler.nvdla(cfg_path)
            total_latency_ns = 0.0
            for layer_spec in model_specs.values():
                if layer_spec.type == LayerTypes.Conv2D:
                    out_size = [
                        1,
                        layer_spec.get(Params.OUT_CHANNELS),
                        layer_spec.get(Params.OUT_HEIGHT),
                        layer_spec.get(Params.OUT_WIDTH),
                    ]
                    padding = 0 if layer_spec.get(Params.PADDING) == "valid" else int(layer_spec.get(Params.KERNEL_SIZE)[0] - 1) / 2
                    input_size = [
                        1,
                        layer_spec.get(Params.IN_CHANNELS),
                        layer_spec.get(Params.IN_HEIGHT),
                        layer_spec.get(Params.IN_WIDTH),
                    ]
                    conv_obj = profiler.Conv2d(
                        nvdla_profiler,
                        log_file,
                        layer_spec.name,
                        out_size,
                        input_size[1],
                        out_size[1],
                        layer_spec.get(Params.KERNEL_SIZE)[0],
                        layer_spec.get(Params.STRIDE)[0],
                        padding,
                        1,
                        layer_spec.get(Params.BIAS),
                    )
                    total_latency_ns += conv_obj.forward(input_size)
                elif layer_spec.type == LayerTypes.Dense:
                    out_size = [1, layer_spec.get(Params.OUT_FEATURES)]
                    dense_obj = profiler.Linear(
                        nvdla_profiler,
                        log_file,
                        layer_spec.name,
                        out_size,
                        layer_spec.get(Params.IN_FEATURES),
                        layer_spec.get(Params.OUT_FEATURES),
                        layer_spec.get(Params.BIAS),
                    )
                    total_latency_ns += dense_obj.forward([1, layer_spec.get(Params.IN_FEATURES)])

            latency = total_latency_ns / (10 ** 9)
            cost = round(cost_par * cfg["area"], 2)
            latency_temp = latency / max_latency
            cost_temp = cost / max_cost
            total_cost = round((cost_temp * weight_cost) + (latency_temp * (1 - weight_cost)), 4)

            current_hw = (latency, cost, total_cost, cfg["name"])
            if best_hw is None or current_hw[2] < best_hw[2]:
                best_hw = current_hw
        except Exception as e:
            print(f"  Error profiling hardware config {cfg['name']}: {e}")

    if best_hw is None:
        print("  Hardware estimate unavailable: no valid NVDLA configuration")

    return best_hw

def select_parent_directory() -> Optional[Path]:
    """
    Ask the user to select the parent folder containing the experiments.
    
    Returns:
        Path of the selected folder or None if cancelled
    """
    if TKINTER_AVAILABLE:
        root = tk.Tk()
        root.withdraw()  # Hide the main window
        
        directory = filedialog.askdirectory(
            title="Select the parent folder containing the experiments",
            initialdir=os.path.expand_user("~")
        )
        
        return Path(directory) if directory else None
    else:
        # Fallback: ask via command line
        print("GUI not available, please enter the path manually")
        path_input = input("Path of the parent folder containing the experiments: ").strip()
        if path_input and Path(path_input).exists():
            return Path(path_input)
        return None


def analyze_all_experiments(parent_dir: Path, output_dir: Optional[Path] = None):
    """
    Analyze all experiments in the parent folder.
    
    Args:
        parent_dir: Parent folder containing the experiments
        output_dir: Output folder (default: parent_dir)
    """
    if output_dir is None:
        output_dir = parent_dir
    
    output_dir.mkdir(parents=True, exist_ok=True)
    
    # Find all subfolders with algorithm_logs
    experiments = []
    for item in parent_dir.iterdir():
        if item.is_dir() and (item / "algorithm_logs").exists():
            experiments.append(item)
    
    if not experiments:
        print(f"No experiments found in {parent_dir}")
        return
    
    print(f"\nFound {len(experiments)} experiments\n")
    
    # Analyze each experiment
    summary_data = []
    
    for exp_dir in sorted(experiments):
        print(f"📈 Analyzing: {exp_dir.name}")
        
        analyzer = ResultsAnalyzer(exp_dir)
        if not analyzer.load_results():
            continue
        
        # Save CSV for this experiment
        if output_dir != parent_dir:
            exp_csv = output_dir / f"{exp_dir.name}_results.csv"
        else:
            exp_csv = exp_dir / f"{exp_dir.name}_results.csv"
        if analyzer.save_experiment_csv(exp_csv):
            print(f"  CSV saved: {exp_csv.name}")
        
        # Collect info for total CSV
        best_result = analyzer.get_best_result()
        if best_result:
            if best_result.flops is None:
                recalculated_flops = _calculate_flops(exp_dir)
                if recalculated_flops is not None:
                    best_result.flops = recalculated_flops

            if any(value is None for value in [best_result.latency, best_result.hw_cost, best_result.hw_total_cost, best_result.hw_config]):
                hw_metrics = _calculate_hardware(exp_dir)
                if hw_metrics is not None:
                    best_result.latency, best_result.hw_cost, best_result.hw_total_cost, best_result.hw_config = hw_metrics
            summary_row = {
                'experiment': exp_dir.name,
                'best_iteration': best_result.iteration,
                'best_accuracy': best_result.accuracy,
                'best_score': best_result.score,
                'best_nparams': best_result.nparams,
                'best_flops': best_result.flops,
                'best_latency': best_result.latency,
                'best_hw_cost': best_result.hw_cost,
                'best_hw_total_cost': best_result.hw_total_cost,
                'best_hw_config': best_result.hw_config,
            }
            
            # Add data from config.yaml with prefix 'config_'
            for key, value in analyzer.config.items():
                if key not in _EXCLUDED_CONFIG_KEYS:
                    summary_row[f'{key}'] = value
            
            summary_data.append(summary_row)
            print(f"  Best result: iteration {best_result.iteration}, "
                  f"accuracy={best_result.accuracy:.4f}, score={best_result.score:.4f}\n")
        else:
            print(f"  No valid results found\n")
    
    # Save total CSV
    if summary_data:
        summary_csv = output_dir / "summary_best_results.csv"
        try:
            with open(summary_csv, 'w', newline='') as f:
                base_columns = [
                    'experiment', 'best_iteration', 'best_accuracy', 'best_score',
                    'best_nparams', 'best_flops',
                    'best_latency', 'best_hw_cost', 'best_hw_total_cost', 'best_hw_config',
                ]
                all_columns = set()
                for row in summary_data:
                    all_columns.update(row.keys())
                extra_columns = sorted(col for col in all_columns if col not in base_columns)
                fieldnames = [col for col in base_columns if col in all_columns] + extra_columns

                writer = csv.DictWriter(f, fieldnames=fieldnames)
                writer.writeheader()
                
                # Sort by score
                sorted_data = sorted(summary_data, key=lambda x: x['experiment'] if x['experiment'] is not None else float('inf'))
                writer.writerows(sorted_data)
            
            print(f"Summary CSV saved: {summary_csv.name}")
        except Exception as e:
            print(f"Error saving summary CSV: {e}")
    else:
        print("No valid experiments loaded")


