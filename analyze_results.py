"""
Script for analyzing the results of tuning experiments.

This script analyzes the log files generated by the modules (flops_module, hardware_module)
and the tuning pipeline, creating:
1. A CSV for each experiment that groups the results
2. A total CSV with the best result for each experiment

Usage:
    python analyze_results.py

The user will be asked to select the parent folder containing the experiments.
"""

